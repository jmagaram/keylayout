use crate::{key::Key, letter::Letter, penalty::Penalty};

fn from_tuple(item: (&str, f32)) -> (Key, Penalty) {
    let (letters, penalty) = item;
    (
        Key::from_iter(letters.chars().map(|c| Letter::new(c))),
        Penalty::new(penalty),
    )
}

pub fn pair_penalties(count: usize) -> Vec<(Key, Penalty)> {
    PAIR_PENALTIES
        .into_iter()
        .take(count)
        .map(|p| from_tuple(p))
        .collect::<Vec<(Key, Penalty)>>()
}

pub fn triple_penalties(count: usize) -> Vec<(Key, Penalty)> {
    TRIPLE_PENALTIES
        .into_iter()
        .take(count)
        .map(|p| from_tuple(p))
        .collect::<Vec<(Key, Penalty)>>()
}

pub fn top_penalties(pairs: usize, triples: usize) -> Vec<Key> {
    let pairs = pair_penalties(pairs)
        .into_iter()
        .map(|(key, _)| key)
        .collect::<Vec<Key>>();
    let triples = triple_penalties(triples)
        .into_iter()
        .map(|(key, _)| key)
        .filter(|triple| !pairs.iter().any(|pair| triple.contains_all(pair)))
        .collect::<Vec<Key>>();
    [pairs, triples].concat()
}

